class Rozwiazanie
{
  public static void main(String[] args)
  {
    int rokUtworzeniaJezykaProgramowaniaC = 1972;
    int rokUtworzeniaJezykaProgramowaniaJava = 1995;

    System.out.println("1972 + 1995 = " + (rokUtworzeniaJezykaProgramowaniaC + rokUtworzeniaJezykaProgramowaniaJava));
    System.out.println("1972 - 1995 = " + (rokUtworzeniaJezykaProgramowaniaC - rokUtworzeniaJezykaProgramowaniaJava));
    System.out.println("1972 * 1995 = " + rokUtworzeniaJezykaProgramowaniaC * rokUtworzeniaJezykaProgramowaniaJava);
    System.out.println("1995 - 1972 = " + (rokUtworzeniaJezykaProgramowaniaJava - rokUtworzeniaJezykaProgramowaniaC));
    System.out.println("1995 / 1972 = " + rokUtworzeniaJezykaProgramowaniaJava / rokUtworzeniaJezykaProgramowaniaC);
  }
}
