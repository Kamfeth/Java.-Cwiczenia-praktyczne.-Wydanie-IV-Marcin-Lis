class Rozwiazanie
{
  public static void main(String[] args)
  {
    int liczbaPierwszaNumer1 = 2;
    int liczbaPierwszaNumer2 = 3;
    int liczbaPierwszaNumer3 = 5;
    int liczbaPierwszaNumer4 = 7;
    int liczbaPierwszaNumer5 = 11;

    System.out.println("3 % 2 = " + liczbaPierwszaNumer2 % liczbaPierwszaNumer1);
    System.out.println("5 % 7 = " + liczbaPierwszaNumer3 % liczbaPierwszaNumer4);
    System.out.println("11 % 7 = " + liczbaPierwszaNumer5 % liczbaPierwszaNumer4);
  }
}
